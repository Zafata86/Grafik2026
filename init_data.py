def load():
    conn = sqlite3.connect(DB_PATH)
    cur = conn.cursor()
    cur.executemany("INSERT OR REPLACE INTO month_settings (year,month,hours_abvg,hours_rde) VALUES (?,?,?,?)", MONTH_SETTINGS)
    tab_map = {r[0]:r[1] for r in cur.execute("SELECT tab_number,id FROM users").fetchall()}
    inserted = 0
    for tab,yr,mo,day,code,ls in SCHEDULE_DATA:
        eid = tab_map.get(tab)
        if not eid: continue
        cur.execute("INSERT OR REPLACE INTO schedule_entries (employee_id,year,month,day,code,leave_status) VALUES (?,?,?,?,?,?)", (eid,yr,mo,day,code,ls))
        inserted += 1
    conn.commit(); conn.close()
    print(f"Заредени {inserted} записа от графика.")

